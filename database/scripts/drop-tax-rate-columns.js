/**
 * Drop tax/GST rate & value columns from operational tables.
 * Keeps branch_list.gst / is_gst_verified / gst_applicable / gst_applicable_after.
 *
 * Safe / idempotent: skips columns that do not exist.
 *
 * Run from SERVER/:
 *   node database/scripts/drop-tax-rate-columns.js
 */
import "dotenv/config";
import pool from "../../db.js";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));

/** @type {{ table: string, columns: string[] }[]} */
const DROPS = [
    { table: "tasks", columns: ["tax_rate", "tax_value"] },
    { table: "invoice", columns: ["tax_rate", "tax_value"] },
    { table: "sale_items", columns: ["tax_perc", "tax_value"] },
    { table: "quotation_items", columns: ["tax_rate", "tax_value"] },
    { table: "compliance_firms", columns: ["tax_rate", "tax_value"] },
    { table: "service_requests", columns: ["tax_rate", "tax_value"] },
    { table: "branch_services", columns: ["gst_rate", "gst_value"] },
];

async function columnExists(conn, table, column) {
    const [rows] = await conn.query(
        `SELECT 1 AS ok
         FROM information_schema.COLUMNS
         WHERE TABLE_SCHEMA = DATABASE()
           AND TABLE_NAME = ?
           AND COLUMN_NAME = ?
         LIMIT 1`,
        [table, column]
    );
    return rows.length > 0;
}

async function main() {
    const conn = await pool.getConnection();
    const dropped = [];
    const skipped = [];

    try {
        for (const { table, columns } of DROPS) {
            for (const column of columns) {
                const exists = await columnExists(conn, table, column);
                if (!exists) {
                    skipped.push(`${table}.${column}`);
                    console.log(`skip  ${table}.${column} (missing)`);
                    continue;
                }
                await conn.query(`ALTER TABLE \`${table}\` DROP COLUMN \`${column}\``);
                dropped.push(`${table}.${column}`);
                console.log(`drop  ${table}.${column}`);
            }
        }

        const sqlPath = path.join(__dirname, "../migrations/20260717_drop_tax_rate_columns.sql");
        const sqlDir = path.dirname(sqlPath);
        if (!fs.existsSync(sqlDir)) fs.mkdirSync(sqlDir, { recursive: true });
        const sql = DROPS.map(({ table, columns }) =>
            columns.map((c) => `-- ALTER TABLE \`${table}\` DROP COLUMN \`${c}\`;`).join("\n")
        ).join("\n\n");
        fs.writeFileSync(
            sqlPath,
            `-- Drop tax/GST amount columns (keep branch_list GST flags)\n-- Generated by drop-tax-rate-columns.js\n\n${sql}\n`,
            "utf8"
        );

        console.log("\nDone.");
        console.log(`Dropped: ${dropped.length}`);
        console.log(`Skipped: ${skipped.length}`);
        console.log(`SQL note: ${sqlPath}`);
    } finally {
        conn.release();
        await pool.end();
    }
}

main().catch((err) => {
    console.error(err);
    process.exit(1);
});
